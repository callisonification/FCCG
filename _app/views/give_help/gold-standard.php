    <p>In partnership with the gaming industry, the Florida Council on Compulsive Gambling, Inc. has created the Gold Standard Responsible Gaming Player Protection Certification Program to recognize facilities that provide environments which promote responsible gaming practices and protect patrons who demonstrate signs of problem or compulsive gambling.</p>
    <br />
    <p>Participation in this program is not mandatory or required by the Department of Business and Professional Regulation (DBPR).  The Gold Standard Program is designed to recognize industry members who choose to demonstrate a higher level of commitment to the industry, staff, patrons and community than required by regulation.</p>
