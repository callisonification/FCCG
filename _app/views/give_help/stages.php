  <p><em>Gambling Addiction has a number of stages to describe how the disorder progresses, as well as how to begin the path to recovery.</em></p>
  <!-- body content here -->
  <div class="body-content">
    <div class="feature-image"> <img src="<?=site_url('assets/site_photos/stages.jpg');?>" class="thumbnail img-responsive" width="100%" /> </div>
    <hr class="divider">
    <p>The onset of problem gambling varies from person to person, and depends largely on the person’s experience when they gamble. It can be important to understand the type of game you play, how it affects you, and why you are continuing to play, even when things in your life begin to fall apart. Gamblers who experience these difficulties often experience their problem in a set of stages.</p>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-info">Winning Stage <small class="pull-right">Stage One</small></h3>
      </div>
      <div class="panel-body">
        <p>In the beginning, the compulsive gambler often experiences a “big win,” which results in more frequent gambling and increased wagers. This tends to enhance self-image, and the gambler begins to fantasize about winning and the wonderful and exciting future this will bring about. Unreasonable optimism develops.</p>
        <p class="text-info"><small>Note:  Some gamblers never experience this phase and skip to the following stages of progression.</small></p>
      </div>
    </div>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-warning">Losing Stage <small class="pull-right">Stage Two</small></h3>
      </div>
      <div class="panel-body">
        <p>During this period, the compulsive gambler thinks only about gambling. Personality changes begin to develop. Lies, borrowing money, and cover-ups are common. The gambler can no longer control the gambling, and relationships with family, friends, and employers deteriorate.</p>
      </div>
    </div>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-warning">Desperation Stage <small class="pull-right">Stage Three</small></h3>
      </div>
      <div class="panel-body">
        <p>The gambler can no longer pay debts and looks for bailouts that could be legal or illegal. These actions are rationalized as a means of getting even or recovering their losses. As the “chase” continues, the gambler panics at the prospect that the action will stop. During this time, the gambler clings to the belief that a winning streak, which will solve all their problems, is just around the corner. There are increasing signs of depression, irritability, and thoughts of suicide may be present.</p>
      </div>
    </div>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-danger">Hopelessness  Stage <small class="pull-right">Stage Four</small></h3>
      </div>
      <div class="panel-body">
        <p>At this point, serious consequences begin to occur, which may seem irreversible, such as arrests, divorce, alcohol or other substance abuse problems, emotional breakdowns, and serious withdrawal symptoms. The gambler realizes that getting even or catching up is not possible, but no longer cares. Approximately 20% of the time, this type of despair may lead the gambler to attempt suicide.</p>
      </div>
    </div>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-danger">Critical  Stage <small class="pull-right">Stage Five</small></h3>
      </div>
      <div class="panel-body">
        <p>This person has reached the rock-bottom of their addiction, and has a newfound resolve to make things better. This stage is characterized by the person making decisions designed to lead to a solution to problems gambling has created. They have an honest desire for help, and an understanding that steps taken to start recovery need to be realistic. They may begin to do things more positively, such as rededicate to work or family life, and will seek to understand life away from gambling activity with higher levels of clarity.</p>
      </div>
    </div>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-success">Rebuilding Stage <small class="pull-right">Stage Six</small></h3>
      </div>
      <div class="panel-body">
        <p>The Rebuilding Stage allows the person to identify new interests, enhance their relationships with friends and family, and begin setting right the wrongs they created in their addiction. A defining feature of this stage is increases in awareness related to their own strengths and weaknesses with their addiction, and finding ways to keep their progress moving forward. The levels of success the problem gambler is having staying away leads to higher levels of self-respect, and richer interactions with others. Measures taken to repair gambling damages can include paying bills, established family financial plans, a budget, resolving legal problems, and experiencing less irritation and stress involved in the problems from the addiction.</p>
      </div>
    </div>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-success">Growth Stage <small class="pull-right">Stage Seven</small></h3>
      </div>
      <div class="panel-body">
        <p>In this stage, the person embraces a new way of life. They keep the insight and awareness they have built through this difficult process as a message for themselves and others. This person is also able to face problems promptly, and head-on, and is experiencing less pre-occupation related to their past gambling, and risk of relapse. The goal of this phase is to continue to grow as a person, and stay strong in all efforts that support a gambling free lifestyle. They are able to live for themselves and others around them, and can be willing to sacrifice or support others in many areas of life. Family relationships continue to grow, and the recovering person is able to show affection in ways that had been lost in the addiction. Generally, this person has lived a gambling free lifestyle for more than six months, and continues to work on growing as an individual in many areas of their life.</p>
      </div>
    </div>
