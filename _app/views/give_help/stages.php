  <p><em>Gambling Addiction has a number of stages to describe how the disorder progresses, as well as how to begin the path to recovery.</em></p>
  <!-- body content here -->
  <div class="body-content">
    <div class="feature-image"> <img src="<?=site_url('assets/site_photos/stages.jpg');?>" class="thumbnail img-responsive" width="100%" /> </div>
    <hr class="divider">
    <p>The onset of problem gambling varies from person to person, and depends largely on the person’s experience when they gamble. It can be important to understand the type of game you play, how it affects you, and why you are continuing to play, even when things in your life begin to fall apart. Gamblers who experience these difficulties often experience their problem in a set of stages.</p>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-info">Winning Stage <small class="pull-right">Stage One</small></h3>
      </div>
      <div class="panel-body">
        <p>In the beginning, the compulsive gambler often experiences a “big win,” which results in more frequent gambling and increased wagers. This tends to enhance self-image, and the gambler begins to fantasize about winning and the wonderful and exciting future this will bring about. Unreasonable optimism develops.</p>
        <p class="text-info"><small>Note:  Some gamblers never experience this phase and skip to the following stages of progression.</small></p>
      </div>
    </div>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-warning">Losing Stage <small class="pull-right">Stage Two</small></h3>
      </div>
      <div class="panel-body">
        <p>During this period, the compulsive gambler thinks only about gambling. Personality changes begin to develop. Lies, borrowing money, and cover-ups are common. The gambler can no longer control the gambling, and relationships with family, friends, and employers deteriorate.</p>
      </div>
    </div>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-danger">Desperation Stage <small class="pull-right">Stage Three</small></h3>
      </div>
      <div class="panel-body">
        <p>The gambler can no longer pay debts and looks for bailouts that could be legal or illegal. These actions are rationalized as a means of getting even or recovering their losses. As the “chase” continues, the gambler panics at the prospect that the action will stop. During this time, the gambler clings to the belief that a winning streak, which will solve all their problems, is just around the corner. There are increasing signs of depression, irritability, and thoughts of suicide may be present.</p>
      </div>
    </div>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-danger">Critical Stage <small class="pull-right">Stage Four</small></h3>
      </div>
      <div class="panel-body">
        <p>At this point, serious consequences begin to occur, which may seem irreversible, such as arrests, divorce, alcohol or other substance abuse problems, emotional breakdowns, and serious withdrawal symptoms. The gambler realizes that getting even or catching up is not possible, but no longer cares. Approximately 20% of the time, this type of despair may lead the gambler to attempt suicide.</p>
      </div>
    </div>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-success">Rebuild Stage <small class="pull-right">Stage Five</small></h3>
      </div>
      <div class="panel-body">
        <p>Beginning the process of trying to rebuild the lives they’ve lost, the action compulsive gambler has to learn new behaviors that don’t involve any form of gambling. Total abstinence from gambling is required – just as it is for recovering alcoholics and substance abusers. There can be no such thing as a little gambling, or gambling for fun. One slip back into gambling is just like the first drop of alcohol, the first hit of a drug – relapse. Tips and techniques, and support from others in like situations (12-step groups) are immensely important in the recovery phase.</p>
      </div>
    </div>
    <br />
    <div class="panel panel-default">
      <div class="panel-heading">
        <h3 class="panel-title text-success">Growth Stage <small class="pull-right">Stage Six</small></h3>
      </div>
      <div class="panel-body">
        <p>The person has stopped gambling for at least six months and is working to hold onto this success. Other problems may emerge as gambling is less of an issue. It’s important to work through these concerns to keep the risk of relapse down. Change efforts may move to new areas, such healing hurt relationships and restoring trust.</p>
      </div>
    </div>
