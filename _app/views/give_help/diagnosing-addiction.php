    <p>Gambling Addiction has a set of criteria used by treatment professionals to identify problems cause by compulsive gambling, as well as identify appropriate ways to begin treatment.</p>
    <p>The American Psychiatric Association (APA) defines Gambling Disorder as a Non-Substance based Addictive Disorder. The following 9 criteria are used by mental health professionals to identify the presence of gambling as a clinical issue:</p><br />
    <ol>
    	<li><strong>Tolerance:</strong> Needs to gamble with increasing amounts of money in order to achieve the desired excitement.</li>
        <br />
        <li><strong>Withdrawal:</strong> Is restless or irritable when attempting to cut down or stop gambling.</li>
        <br />
        <li><strong>Relapse:</strong> Has repeated unsuccessful efforts to control, cut back, or stop gambling.</li>
        <br />
        <li><strong>Preoccupation:</strong> Is often preoccupied with gambling (e.g., having persistent thoughts of reliving past gambling experiences, handicapping or planning next venture, thinking of ways to get money with which to gamble).</li>
        <br />
        <li><strong>Escape:</strong> Often gambles when feeling distressed (e.g., helpless, guilty, anxious, depressed).</li>
        <br />
        <li><strong>Chasing:</strong> After losing money gambling, often returns another day to get even.</li>
        <br />
        <li><strong>Lying to Loved Ones:</strong> Lies to conceal the extent of involvement with gambling.</li>
        <br />
        <li><strong>Risking Relationships:</strong> Has jeopardized or lost a significant relationship, job, or educational or career opportunity because of gambling.</li>
        <br />
        <li><strong>Bailouts/Relying on Others for Money:</strong> Relies on others to provide money to relieve a desperate financial situation caused by gambling.</li>
    </ol>
    <br />
    <p>If you have experienced more than one of these in the past 12 months, it may be helpful to call the HelpLine to receive resources and additional information.</p>
